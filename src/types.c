#include "include/types.h"

inline ivec2 newIVec2(i32 x, i32 y) {
    return (ivec2){x, y};
}

inline ivec3 newIVec3(i32 x, i32 y, i32 z) {
    return (ivec3){x, y, z};
}

inline fvec2 newFVec2(float x, float y) {
    return (fvec2){x, y};
}

inline fvec3 newFVec3(float x, float y, float z) {
    return (fvec3){x, y, z};
}
